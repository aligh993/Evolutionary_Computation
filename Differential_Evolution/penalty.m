function pen=penalty(p,d,t,c,alpha,beta)

if d==2
    if (p(1,1)-2*p(1,2)+1==0)&&((p(1,2)^2)-(p(1,1)^2)/4-1<=0)
        pen=0;
    else
        pen=((c*t)^alpha)*((abs(p(1,1)-2*p(1,2)+1)^beta)+(abs((p(1,2)^2)-(p(1,1)^2)/4-1)^beta));
    end
end

if d==5
    g1=-1*(85.334407+0.0056858*p(1,2)*p(1,5)+0.00026*p(1,1)*p(1,4)-0.0022053*p(1,3)*p(1,5));
    g2=85.334407+(0.0056858*p(1,2)*p(1,5))+(0.00026*p(1,1)*p(1,4))-(0.0022053*p(1,3)*p(1,5))-92;
    g3=-1*(80.51249+0.0071317*p(1,2)*p(1,5)+0.00029955*p(1,1)*p(1,2)+0.0021813*p(1,3)^2-90);
    g4=80.51249+(0.0071317*p(1,2)*p(1,5))+(0.00029955*p(1,1)*p(1,2))+(0.0021813*p(1,3)^2)-110;
    g5=-1*(9.300961+0.0047026*p(1,3)*p(1,5)+0.0012547*p(1,1)*p(1,3)+0.0019085*p(1,3)*p(1,4)-20);
    g6=9.300961+0.0047026*p(1,3)*p(1,5)+0.0012547*p(1,1)*p(1,3)+0.0019085*p(1,3)*p(1,4)-25;
    
    if ((g1<=0)&&(g2<=0)&&(g3<=0)&&(g4<=0)&&(g5<=0)&&(g6<=0))
        pen=0;
    else
        pen=((c*t)^alpha)*((abs(g1))^beta+(abs(g2))^beta+abs(g3)^beta+(abs(g4))^beta+(abs(g5))^beta+(abs(g6))^beta);
    end
end

if d==10
    if (p(1,2)+2*p(1,2)+2*p(1,3)+p(1,6)+p(1,10)-2==0)&&(p(1,4)+2*p(1,5)+p(1,6)+p(1,7)-1==0)&&(p(1,3)+p(1,7)+p(1,8)...
            +2*p(1,9)+p(1,10)-1==0)
        pen=0;
    else
        pen=((c*t)^alpha)*((abs(p(1,2)+2*p(1,2)+2*p(1,3)+p(1,6)+p(1,10)-2)^beta)+(abs(p(1,4)+2*p(1,5)+p(1,6)+p(1,7)...
            -1)^beta)+(abs(p(1,3)+p(1,7)+p(1,8)+2*p(1,9)+p(1,10)-1)^beta));
    end
end
end